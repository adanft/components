import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { createHighlighter } from 'shiki';
import ts from 'typescript';

const docsRoot = new URL('..', import.meta.url);
const sourceRoot = new URL('src', docsRoot);
const outputFile = new URL('src/generated/highlighted-code.ts', docsRoot);
const theme = 'catppuccin-mocha';
const lang = 'tsx';

function hashCode(value) {
  let hash = 2166136261;

  for (let index = 0; index < value.length; index += 1) {
    hash ^= value.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }

  return (hash >>> 0).toString(36);
}

async function collectSourceFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(directory, entry.name);

      if (entry.isDirectory()) {
        if (entry.name === 'generated') {
          return [];
        }

        return collectSourceFiles(fullPath);
      }

      return /\.(tsx|ts)$/.test(entry.name) ? [fullPath] : [];
    }),
  );

  return files.flat();
}

function getExpressionText(expression) {
  if (ts.isStringLiteral(expression) || ts.isNoSubstitutionTemplateLiteral(expression)) {
    return expression.text;
  }

  return undefined;
}

function collectCodeBlockIdentifiers(sourceFile) {
  const identifiers = new Set();

  function visit(node) {
    if (ts.isJsxSelfClosingElement(node) || ts.isJsxOpeningElement(node)) {
      const tagName = node.tagName.getText(sourceFile);

      if (tagName === 'CodeBlock') {
        for (const attribute of node.attributes.properties) {
          if (
            ts.isJsxAttribute(attribute) &&
            attribute.name.text === 'code' &&
            attribute.initializer &&
            ts.isJsxExpression(attribute.initializer) &&
            attribute.initializer.expression &&
            ts.isIdentifier(attribute.initializer.expression)
          ) {
            identifiers.add(attribute.initializer.expression.text);
          }
        }
      }
    }

    ts.forEachChild(node, visit);
  }

  visit(sourceFile);
  return identifiers;
}

function collectSnippetValues(sourceFile, identifiers) {
  const snippets = [];

  function visit(node) {
    if (
      ts.isVariableDeclaration(node) &&
      ts.isIdentifier(node.name) &&
      identifiers.has(node.name.text) &&
      node.initializer
    ) {
      const value = getExpressionText(node.initializer);

      if (value !== undefined) {
        snippets.push(value);
      }
    }

    ts.forEachChild(node, visit);
  }

  visit(sourceFile);
  return snippets;
}

const files = await collectSourceFiles(sourceRoot.pathname);
const snippets = new Set();

for (const filePath of files) {
  const source = await readFile(filePath, 'utf8');
  const sourceFile = ts.createSourceFile(
    filePath,
    source,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TSX,
  );
  const identifiers = collectCodeBlockIdentifiers(sourceFile);

  for (const snippet of collectSnippetValues(sourceFile, identifiers)) {
    snippets.add(snippet);
  }
}

const highlighter = await createHighlighter({
  langs: [lang],
  themes: [theme],
});

const highlightedCodeByHash = Object.fromEntries(
  Array.from(snippets, (code) => [
    hashCode(code),
    highlighter.codeToHtml(code, {
      lang,
      theme,
    }),
  ]),
);

const generatedSource = `// This file is generated by apps/docs/scripts/generate-highlighted-code.mjs. Do not edit manually.\n\nconst highlightedCodeByHash = ${JSON.stringify(
  highlightedCodeByHash,
  null,
  2,
)} as const;\n\nfunction hashCode(value: string) {\n  let hash = 2166136261;\n\n  for (let index = 0; index < value.length; index += 1) {\n    hash ^= value.charCodeAt(index);\n    hash = Math.imul(hash, 16777619);\n  }\n\n  return (hash >>> 0).toString(36);\n}\n\nexport function getHighlightedCode(code: string) {\n  return highlightedCodeByHash[hashCode(code) as keyof typeof highlightedCodeByHash];\n}\n`;

await mkdir(new URL('.', outputFile), { recursive: true });
await writeFile(outputFile, generatedSource);

console.log(`Generated ${snippets.size} highlighted code snippets.`);
